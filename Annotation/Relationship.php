namespace RonteLtd\JsonApiBundle\Annotation;

/**
 * Relationship
 *
 * @author Alexey Astafev <efsneiron@gmail.com>
 * @Annotation
 */
class Relationship
{
    /**
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name)
    {
        $this->name = $name;
    }
}